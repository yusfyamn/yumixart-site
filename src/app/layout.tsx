import type { Metadata } from "next";
import "./globals.css";

export const metadata: Metadata = {
  title: "Klymk AI - AI-Powered Video Creation for Social Media",
  description: "Create viral Instagram and TikTok content with advanced AI. Remove objects, edit scenes, and manipulate videos at a fraction of traditional costs.",
};

export default function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  return (
    <html lang="en" className="dark">
      <body className="font-sans antialiased">
        <script
          dangerouslySetInnerHTML={{
            __html: `
              (function() {
                try {
                  var theme = localStorage.getItem('theme') || 'dark';
                  if (theme === 'dark') {
                    document.documentElement.classList.add('dark');
                  } else {
                    document.documentElement.classList.remove('dark');
                  }
                } catch (e) {
                  document.documentElement.classList.add('dark');
                }
              })();
            `,
          }}
        />
        {children}
      </body>
    </html>
  );
}
